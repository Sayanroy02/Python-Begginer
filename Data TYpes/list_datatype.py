# a list is collection of items . Eg:- ["hello", "World"]

#LIST DATATYPE
my_list =["Sayan" , "Roy" , "Guptra" , "22"]  # all the items are in list 
print(my_list)   # printing the list 

#TO FIND THE LENGTH OF LIST
print(len(my_list))  #use len function to find the length 

# TO ADD SOMETHING IN THE LIST 
my_list.append("python")  #we can add items by using append () and put data inside the inverted commas inside brackets  .
print(my_list)

#TO REMOVE A ELEMENT FROM LIST 
my_list.remove("Roy")     #we can add items by using remove() and put whatever data inside the inverted commas inside brackets  you want to remove.
print(my_list)

